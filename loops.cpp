#include<iostream>
using namespace std;
int main()


                  // while loop
{
    int i = 1;
    while(i<=5)
    {
        cout <<"we are in the loop" << i <<endl;
        i++;
    }
}



                    // do while loop
{
    int i=1;
    do
    {
        cout <<"we are in the loop" << i <<endl;
        i++;
    } while (i<=5);
    
}





                       // for loop
{
    for (int i=1; i<=5; i++)
    {
        cout <<"we are in the loop " << i << endl;
    }
}